package tek.week_11.day_2;

public class IncrementDecrement {

    public static void main(String[] args) {

        int number = 4;
        System.out.println( number++ );
        System.out.println( number );

        System.out.println( ++number );

        System.out.println( number-- );
        System.out.println( number );

        System.out.println( --number );
    }

}
