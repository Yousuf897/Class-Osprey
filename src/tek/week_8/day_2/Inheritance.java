public class Inheritance {

     /*   Real-life Analogy:

    Think of inheritance in terms of biological families. Children inherit traits and characteristics from their parents.
    For instance, you might have your mother's eyes and your father's nose. Similarly, a kitten might inherit its fur color
    from its parents. This passing down of attributes is a form of inheritance in the real world.

    Programming Explanation:

    In programming, especially in object-oriented programming, inheritance is a mechanism where a new class inherits
    properties (attributes) and behaviors (methods) from an existing class. This provides an opportunity to promote code reuse
    and establish a relationship between the parent (superclass) and child (subclass) classes.

    Using inheritance, the child class not only inherits the members of the parent class but can also have additional members
    (attributes and methods). The primary advantage is that it allows developers to create a new class based on an existing
    class without rewriting the same code.

    In Java, the extends keyword is used to denote inheritance from a superclass.

    Types of Inheritance:

    There are different types of inheritance, depending on the number of superclasses and subclasses:

    1. Single Inheritance: A subclass inherits from a single superclass.

    2. Multiple Inheritance (Interface): A class inherits from more than one class. Java does not support multiple
    inheritances with classes. However, multiple inheritance in Java is achieved through interfaces.

    3. Multilevel Inheritance: A class inherits from a subclass, making it a multilevel inheritance chain. For example,
    if Class C inherits from Class B, which in turn inherits from Class A, it's a multilevel inheritance.

    4. Hierarchical Inheritance: Multiple classes inherit from a single class.

    5. Hybrid Inheritance: A combination of two or more types of inheritance. It's not supported in Java
    through class-based inheritance (due to the "diamond problem"), but it can be mimicked using interfaces.
*/

}
