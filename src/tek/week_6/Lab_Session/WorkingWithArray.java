package tek.week_6.Lab_Session;

public class WorkingWithArray {

    void printValuesOfArray( String[] array ) {
        for (String arrayValue : array ) {
            System.out.println( arrayValue );
        }
    }

    void printValuesOfIntArray( int[] array ) {
        for (int arrayValue : array ) {
            System.out.println( arrayValue );
        }
    }

}
