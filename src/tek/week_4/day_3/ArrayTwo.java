package tek.week_4.day_3;

public class ArrayTwo {

    public static void main(String[] args) {

        // Static Implementation of 1D array
        String[] names = { "Bob" , "Alex" , "Lydia" , "Eric", "Trevor" , "Alixe"  };

        // What is the size of this array!
        System.out.println( names.length );

        // How to print a single value from the 1D array
        System.out.println( names[0] );

        String firstLocation = names[0];

        System.out.println(firstLocation);

        String secondLocation = names[1];

        System.out.println(secondLocation);

    }
}
