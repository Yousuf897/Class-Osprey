package tek.week_2.day_1;

public class Operators {

    public static void main(String[] args) {

        int numberOne = 50;
        int numberTwo = 100;

        int result = numberOne + numberTwo;
        //   result      =     50     +  100 = 150

        System.out.println( result );

        System.out.println( numberOne + numberTwo );

        int numberThree = result + 20;

        System.out.println( numberThree );

        System.out.println( 70 + 30 );

        System.out.println( 100 + 650 + 1000 + 54000 + 100 + 1);

        System.out.println( "This is my number: " + (2 + 3)  );

        //        String + 23

        System.out.println( 50 + 60  + " This is my number: ");

        //         100 + String

        // 5 + 8 + 1 - 20 * [ 2 + ( 2 + 5 ) + 4 ]

        System.out.println( " * * * * * * * * * * * * * * * * * * * * * * * *" );

        int numberFour = 50;
        int numberFifth = 25;

        int subResult = numberFour - numberFifth;
        //   subResult = 25

        System.out.println( 100 - 50 - 25 - 5 - 10 - 20  );

        System.out.println( 50 - 100 );

        System.out.println( " * * * * * * * * * * * * * * * * * * * * * * * *" );

        int mulNumberOne = 50;
        int mulNumberTwo = 2;

        int mulResult = mulNumberOne * mulNumberTwo;

        System.out.println( mulResult );

        System.out.println( 50 * 2 * 10 * 100 * 1000 );

        System.out.println( " * * * * * * * * * * * * * * * * * * * * * * * *" );

        int divNumberOne = 50;
        int divNumberTwo = 2;

        int divResult = mulNumberOne / mulNumberTwo;

        System.out.println( divResult );

        System.out.println( 1000 / 100 / 2);

        System.out.println( " * * * * * * * * * * * * * * * * * * * * * * * *" );

        int numbOne = 10;
        int numbTwo = 3;

        int modulusResult = numbOne % numbTwo;

        System.out.println( modulusResult );

        System.out.println( " * * * * * * * * * * * * * * * * * * * * * * * *" );

    }

}
