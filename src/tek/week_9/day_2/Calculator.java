package tek.week_9.day_2;

public class Calculator {

    public int sum(int numberOne, int numberTwo) {
        return  numberOne + numberTwo;
    }

    public int sum( int numberOne, int numberTwo, int numberThree ) {
        return  numberOne + numberTwo + numberThree;
    }

    public int sum( int numberOne, int numberTwo, int numberThree, int numberFour ) {
        return  numberOne + numberTwo + numberThree + numberFour;
    }

    public int sum( int numberOne, int numberTwo, int numberThree, int numberFour, int numberFive ) {
        return  numberOne + numberTwo + numberThree + numberFour + numberFive;
    }

}
